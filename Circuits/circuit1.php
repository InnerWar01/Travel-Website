	<h2>De Melbourne à Adélaïde</h2>
	<p class="section-text">
		Depuis Melbourne, empruntez la spectaculaire Great Ocean Road jusqu’à Adélaïde. 
		Ce trajet prendra cinq jours avec des étapes touristiques, ou deux jours sans s’arrêter. 
		Visitez l’emblématique spot de surf de Bells Beach, promenez-vous en forêt jusqu’aux 
		cascades du parc national d’Otway, ou bien laissez-vous émerveiller par les majestueuses 
		formations de calcaire des Douze Apôtres.  Passez vos nuits dans des villes portuaires 
		comme Lorne, Apollo Bay ou Port Fairy, où l’on bifurque vers l’intérieur du pays pour 
		aller voir les Grampians.  Passez la nuit à Halls Gap, qui offre un accès facile à la faune, 
		les sentiers de promenade et les sites d’art rupestre aborigène du parc national des Grampians.  
		Allez faire de l’escalade sur le Mt Arapiles, un peu plus loin.  En Australie méridionale, 
		il est possible de faire un détour vers les terres intérieures pour visiter la région viticole 
		de Coonawarra ou pour voir les fossiles des grottes de Naracoorte, classées au patrimoine mondial.  
		Dégustez un homard à Kingston ou descendez en canoë-kayak le réseau de cours d’eau tranquilles du Coorong.  
		Continuez jusqu’à l’élégante ville d’Adélaïde, bordée de plages, de collines et des vignobles de la Barossa 
		Valley.  Explorez les musées et les bibliothèques de North Terrace, dînez dans l’une des « rues réservées 
		pour manger » ou bien pique-niquez dans l’un des jardins qui couvrent presque la moitié de la ville.   
		Faites du vélo dans le parc botanique ou allez ramer à travers les roseraies de Rymill Park.  
		Nagez avec les dauphins ou apprenez à faire de la voile à Glenelg, ou bien allez pêcher depuis 
		la jetée à Henley.
	</p>
