		Ensuite, vous pouvez louer une voiture pour aller explorer la Tasmanie du Nord au Sud.  
		Allez admirer les courbes parfaites de la baie de Wineglass dans le parc national de Freycinet.  
		Offrez-vous une baignade à St Helens et suivez le sentier de randonnée du Bay of Fires Walk dans 
		le parc national de Mount William.  Admirez la vue sur Launceston depuis Cataract Gorge et faites 
		du vélo en bord de mer à Devonport.  Apaisez-vous l’esprit dans la nature sauvage du parc national 
		de Cradle Mountain/Lake St Clair, inscrit au Patrimoine mondial.  Passez la nuit à Strahan et 
		faites un tour en bateau sur la Gordon River ou du rafting sur les eaux tumultueuses de la Franklin.  
		Promenez-vous jusqu’aux cascades et aux forêts de fougères arborescentes dans le parc national de 
		Mount Field.   Que vous arriviez en Tasmanie par le Nord ou le Sud, peu importe : on peut faire 
		le tour de l’île dans n’importe quelle direction.
	</p>
</div>

