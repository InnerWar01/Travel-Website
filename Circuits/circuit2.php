	<h2>De Broken Hill à Adélaïde</h2>
	<p class="section-text">
        La première chose que vous remarquerez en vous arrêtant dans la cité australienne des festivals, 
        ce sont les bâtiments coloniaux entourés de superbes parcs, plages et collines.  
        Allez voir les bâtiments de pierre le long de North Terrace et les cafés de Rundle Street.  
        Regardez en passant la colline de Montefiore Hill, le belvédère du Colonel Light's Lookout, 
        le terrain de cricket (Adelaide Oval Cricket Ground) et le lac Torrens.
        Vous pouvez vous promener dans l’un des avant-postes les plus isolés du monde pendant 
        le court arrêt à Cook.  Ancienne colonie prospère grâce à la voie ferrée, 
        Cook ne compte plus aujourd’hui qu’une poignée de résidents et se situe au milieu de 
        la vaste plaine sans arbres du Nullarbor, à quelque 1 500 km de Perth.
        Descendez dans la capitale australienne de l’or juste à temps pour une virée de nuit.  
        Allez voir la mine d’or du Superpit, profonde de plus de 400 m et longue de 3,3 km, 
        sous la lumière des projecteurs, avec d’énormes camions qui ressemblent, vus de loin, … à des jouets pour enfants.  
        Ecoutez des histoires de bandits, de maisons closes et de fortunes gagnées et perdues en vous promenant devant 
        les vieux pubs élégants de Hannan Street.  La rue doit son nom à l’Irlandais Paddy Hannan qui fut le premier 
        à découvrir les richesses de la région en 1893.
	</p>
