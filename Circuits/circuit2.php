        Allez voir les sculptures du désert vivant ou l’une des remarquables galeries d’art.  
        S’il vous semble que la terre bouge, ne vous inquiétez pas.  
        Après une journée de voyage, vous ressentez encore probablement le balancement rythmique du train sous vos pieds.
