	<p class="section-text">
        Profitez de ces deux journées pour vous dégourdir les jambes après cette traversée inoubliable. 
        Sous le soleil, dans les parcs naturels et sur les plages de Perth, vous découvrirez l'une des plus belles 
        villes du pays et la capitale de l'État d'Australie Occidentale. 
        Des autobus gratuits vous emmèneront autour du centre-ville, où vous aurez la possibilité de visiter 
        la tour de Swan Bells, l'Hôtel de la Monnaie de Perth, la galerie d’art d'Australie Occidentale (Art Gallery of WA) 
        et de nombreuses autres attractions.
        Pour ceux davantage attirés par le shopping, dirigez-vous vers la rue chic de King Street. 
        Bordée de boutiques, de galeries d'art et autres restaurants, c'est l'endroit où 
        se rendre pour être vu. Envie d'un déjeuner tranquille? Faites votre choix parmi une avalanche 
        de cafés et restaurants au bord de la rivière ou de la mer.
        Promenez-vous dans le jardin botanique de Kings Park ou aventurez-vous sur 
        le « chemin de la cime des arbres » pour un panorama unique sur la ville. 
        On trouve des barbecues fixes pouvant être librement utilisés et de nombreuses aires 
        de pique-nique pour se faire un repas au milieu des eucalyptus.
        Amateurs de sport ? Pourquoi ne pas vous rendre à un match de cricket, de football australien 
        ou de rugby dans l'un des stades de la ville ?
        Montez à bord d'un ferry à destination du sud de Perth pour pouvoir prendre vous-même une photo 
        de la célèbre vue panoramique de Perth. C’est un endroit agréable, longé de pelouses nettes, 
        de palmiers et de cafés en bord de l'eau, idéal pour se reposer en déjeunant à midi sans trop se presser. 
